#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	int		i;
	size_t	j;
	char	*f;

	f = s;
	i = 0;
	j = 0;
	while (j < n)
	{
		f[i] = 0;
		i++;
		j++;
	}
}
