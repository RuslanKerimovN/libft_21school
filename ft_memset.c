#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	int		i;
	size_t	j;
	char	*f;

	f = b;
	i = 0;
	j = 0;
	while (j < len)
	{
		f[i] = c;
		j++;
		i++;
	}
	return (b);
}
